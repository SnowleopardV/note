// 重排（Layout）或回流（Reflow）：
// 1. 当DOM树中元素的位置、尺寸或其他影响布局的属性发生变化时，浏览器需要重新计算元素的几何属性（如位置和大小），这个过程称为重排或回流。
// 2. 回流通常涉及到构建一个新的布局树（或称为渲染树），这是一个相对昂贵的操作，因为它可能会影响到周围元素的位置和尺寸。

// 重绘（Repaint）：
// 1. 当一个元素的外观属性（如颜色、背景、边框等）发生变化，但不影响其几何布局时，浏览器只需要更新该元素的像素信息，这个过程称为重绘。
// 2. 重绘比回流的开销小，因为它不需要重新计算布局。

// 两者关系:
// 1. 在实际操作中，回流往往会导致重绘，因为元素的布局变化通常会影响到其视觉表现。
// 2. 非所有的重绘都需要回流。例如，当只改变一个元素的颜色而不影响其尺寸或位置时，只会发生重绘而不会触发回流。

// 为了优化网页性能，应尽量减少不必要的回流和重绘：
// 1. 避免在循环中修改样式，可以先将所有更改收集到一个对象中，然后一次性应用。
// 2. 使用 requestAnimationFrame 或 setTimeout(fn, 0) 来批量处理多个修改操作。
// 3. 使用CSS3硬件加速特性，如 transform 和 opacity，它们通常不会触发回流。
